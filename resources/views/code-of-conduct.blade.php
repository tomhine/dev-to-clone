            <x-typo.title>Code of Conduct</x-typo.title>
            <p>All participants of DEV Community are expected to abide by our Code of Conduct, both online and during
                in-person events that are hosted and/or associated with DEV Community.</p>
        </x-typo.section>
        <x-typo.section>
            <x-typo.sub-heading>Our Pledge</x-typo.sub-heading>
            <p>In the interest of fostering an open and welcoming environment, we as moderators of <x-link.a
                    class="underline" href="{{ route('home') }}">DEV Community</x-link.a> pledge
                to make participation in our project and our community a harassment-free experience for everyone,
                regardless of age, body size, disability, ethnicity, gender identity and expression, level of
                experience, nationality, personal appearance, race, religion, or sexual identity and orientation.</p>
        </x-typo.section>
        <x-typo.section>
            <x-typo.sub-heading>Our Standards</x-typo.sub-heading>
            <p>Examples of behavior that contributes to creating a positive environment include:</p>
            <x-typo.u-list>
                <li>Using welcoming and inclusive language</li>
                <li>Being respectful of differing viewpoints and experiences</li>
                <li>Referring to people by their preferred pronouns and using gender-neutral pronouns when uncertain
                </li>
                <li>Gracefully accepting constructive criticism</li>
                <li>Focusing on what is best for the community</li>
                <li>Showing empathy towards other community members</li>
            </x-typo.u-list>
            <p>Examples of unacceptable behavior by participants include:</p>
            <x-typo.u-list>
                <li>The use of sexualized language or imagery and unwelcome sexual attention or advances</li>
                <li>Trolling, insulting/derogatory comments, and personal or political attacks</li>
                <li>Public or private harassment</li>
                <li>
                    Publishing others' private information, such as a physical or electronic address, without explicit
                    permission
                </li>
                <li>Other conduct which could reasonably be considered inappropriate in a professional setting</li>
                <li>Dismissing or attacking inclusion-oriented requests</li>
            </x-typo.u-list>
            <p>We pledge to prioritize marginalized people’s safety over privileged people’s comfort. We will not
                act on
                complaints regarding:</p>
            <x-typo.u-list>
                <li>‘Reverse’ -isms, including ‘reverse racism,’ ‘reverse sexism,’ and ‘cisphobia’</li>
                <li>
                    Reasonable communication of boundaries, such as 'leave me alone,' 'go away,' or 'I’m not discussing
                    this
                    with you.'
                </li>
                Criticisms of racist, sexist, cissexist, or otherwise oppressive behavior or assumptions
                <li>Criticisms of racist, sexist, cissexist, or otherwise oppressive behavior or assumptions</li>
            </x-typo.u-list>
        </x-typo.section>
        <x-typo.section>
            <x-typo.sub-heading>Enforcement</x-typo.sub-heading>
            <p>Violations of the Code of Conduct may be reported by contacting the team via the <x-link.a href="#"
                    class="underline">abuse
                    report form</x-link.a> or by
                sending an email to <x-link.a href="#" class="underline">yo@dev</x-link.a>.to. All reports will be
                reviewed and
                investigated and will result in a
                response that is deemed necessary and appropriate to the circumstances. Further details of specific
                enforcement policies may be posted separately.</p>
            <p>Moderators have the right and responsibility to remove comments or other contributions that are not
                aligned to this Code of Conduct, or to suspend temporarily or permanently any members for other
                behaviors that they deem inappropriate, threatening, offensive, or harmful.</p>
        </x-typo.section>
